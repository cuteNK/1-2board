#include <stdio.h>

void main(void)
{
	int num1 = 3;
	int a = num1;
	printf("num1은 %d, a는 %d\n", num1, a);
	a = 4;
	printf("num1은 %d, a는 %d\n", num1, a);

	int num2 = 30;
	int* ptr = &num2;	// 선언 할 때 주소연산자하기
	printf("num2는 %d, ptr는 %p\n", num2, ptr);	//num2의 값과 ptr의 값(num2의 주소값) 출력

	// ptr이 가리키는 변수(num2)의 내부값을 40으로 바꾼다.
	*ptr = 40;	// num2의 값이 바뀜.
	printf("num2는 %d, ptr는 %p\n", num2, ptr);	// num2의 값을 포인터를 통해 간접적으로 바꿈. ptr의 주소값은 그대로

	// 변수 num2의 내부값을 50으로 바꾼다.
	num2 = 50;
	printf("num2는 %d, ptr는 %p\n", num2, ptr);	// num2의 값을 직접적으로 바꿈. ptr의 주소값은 그대로
}